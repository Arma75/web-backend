-- FOOD_NUTRIENT Table Creation Script
CREATE TABLE FOOD_NUTRIENT (
    ID BIGSERIAL,
    COMPANY VARCHAR(100),
    FOOD VARCHAR(100) NOT NULL,
    SERVE_SIZE INTEGER,
    UNIT VARCHAR(10),
    CALORIE BIGINT,
    CARBOHYDRATE NUMERIC,
    PROTEIN NUMERIC,
    FAT NUMERIC,
    USE_YN CHAR DEFAULT 'Y',
    REG_DTM TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPD_DTM TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (ID)
);

COMMENT ON TABLE FOOD_NUTRIENT IS '식품 영양소 테이블';
COMMENT ON COLUMN FOOD_NUTRIENT.ID IS '아이디(PK)';
COMMENT ON COLUMN FOOD_NUTRIENT.COMPANY IS '제조사';
COMMENT ON COLUMN FOOD_NUTRIENT.FOOD IS '식품명';
COMMENT ON COLUMN FOOD_NUTRIENT.SERVE_SIZE IS '1회 제공량';
COMMENT ON COLUMN FOOD_NUTRIENT.UNIT IS '단위';
COMMENT ON COLUMN FOOD_NUTRIENT.CALORIE IS '칼로리';
COMMENT ON COLUMN FOOD_NUTRIENT.CARBOHYDRATE IS '탄수화물';
COMMENT ON COLUMN FOOD_NUTRIENT.PROTEIN IS '단백질';
COMMENT ON COLUMN FOOD_NUTRIENT.FAT IS '지방';
COMMENT ON COLUMN FOOD_NUTRIENT.USE_YN IS '사용여부';
COMMENT ON COLUMN FOOD_NUTRIENT.REG_DTM IS '등록일시';
COMMENT ON COLUMN FOOD_NUTRIENT.UPD_DTM IS '수정일시';